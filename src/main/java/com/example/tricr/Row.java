package com.example.tricr;

public class Row {
    public String x;
    public String y;
    public String r;
    public boolean inn;
    public String time;
    public String runtime;

    public Row(String x, String y, String r, boolean inn, String time, String runtime) {
        this.x = x;
        this.y = y;
        this.r = r;
        this.inn = inn;
        this.time = time;
        this.runtime = runtime;
    }
}
